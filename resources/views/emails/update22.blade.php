                        <td>
                            <p style="font-family: sans-serif; font-size: 18px;">More good news!</p>
                            <p style="font-family: sans-serif; font-size: 18px;">Today, we released a new tool to add boxes to identify litter in images and we need your help.</p>
                            <ul style="font-family: sans-serif; font-size: 19px;">
                                <li>
                                    <p>At the moment, the tool not yet open to the public but if you would like to become a beta tester please get in touch!</p>
                                    <p>You can see a video about how it works <a href="https://www.reddit.com/r/openlittermap/comments/m7zfup/coming_very_soon_get_rewarded_with_littercoin_by/">here</a></p>
                                    <p>This work is redeemable in Littercoin! If you can help us add 100 boxes, you will earn 1 Littercoin. Any boxes will have to be verified by an Admin before your reward is generated.</p>
                                    <p>We need to add boxes to images to teach the OpenLitterAi, a computer vision algorithm, where the litter is in the image and what each type of litter looks like. This will hopefully automate tagging and it might even remove the need to upload photos.</p>
                                    <p>There is over 100,000 images to tag, and we have yet to pull images from the internet to increase our database. So your help, input and advice here is very welcome and badly needed!</p>
                                    <p>Once we pass beta testing this tool will become publicly available for everyone to help with and contribute to. You can see the code for it <a href="https://github.com/OpenLitterMap/openlittermap-web/pull/195">here</a>.</p>
                                </li>

                                <li>
                                    <p>Our mobile app is now available in Arabic, Dutch, French, German, Spanish and Portuguese!</p>
                                    <p>Can you help us translate into more languages? You can find the mobile app files <a href="https://github.com/OpenLitterMap/react-native/tree/master/assets/langs">here</a> and the website files <a href="https://github.com/OpenLitterMap/openlittermap-web/tree/master/resources/js/langs">here</a></p>
                                    <p>OpenLitterMap.com the website is currently only available in English and Dutch. We would love help getting this into more languages!</p>
                                </li>

                                <li>
                                    <p>We have passed 120,000 uploads and we are now generating about 1,000 uploads per week! A new superuser representing TeamLitterUSA is trying to get into 1st place but will the Dutch allow this to happen?</p>
                                </li>

                                <li>
                                    <p>On community request, we released the <a href="https://github.com/OpenLitterMap/react-native/releases/tag/2.1.5">APK</a> for the mobile app to allow Android users to install the app outside of the Google ecosystem.</p>
                                </li>

                                <li>
                                    <p>I made a presentation to an online GIS discord group which was well received but unfortunately the recording didn't work out!</p>
                                </li>

                                <li>
                                    <p>Thanks for our first corporate sponsor last quarter, we hired our first developer, a girl from Serbia, to work on the mobile app. The developer helped us introduce Swipe between images while tagging, persist non-uploaded or captured images after closing the app, helped us introduce other alphabets like Arabic and special characters to allow for Spanish and Portugese and more!</p>
                                </li>

                                <li>
                                    <p>Back on web, we introduced a new user Dashboard that shows you how much litter you uploaded and gives you cool stats about your contribution to OpenLitterMap. You can now download all your own data too, as well as everyone elses <3</p>
                                </li>

                                <li>
                                    <p>Thanks to open source community work, several bugs were fixed like missing tags added and allow guests to download data for any country without having to sign up for an account.</p>
                                </li>

                                <li>
                                    <p>And probably more?? There is definitely a lot more to come. We are barely scratching the surface here but hopefully in the next update <strong>we might have 1.0 of the OpenLitterAi</strong></p>
                                    <p>In the mean time, we need as much images uploaded as possible. We also need help tagging boxes to images so if this is something you would like to help with please get in touch and I will give you access. All you need is a laptop and some spare time.</p>
                                </li>

                                <li>
                                    <p>As we transition into a post-covid world, hopefully we can start to organise more social-distance friendly outdoor activities. Currently, not even a university campus has had their litter mapped yet. If we organised people to work together in self-isolation, it would probably take about 15 minutes. Would you like to organise an event? Chat with others interested in the future of OLM? If so, please join our slack channel!</p>
                                    <p>
                                        <a href="https://join.slack.com/t/openlittermap/shared_invite/zt-fdctasud-mu~OBQKReRdC9Ai9KgGROw">Click here to join the OpenLitterMap Slack - We want to hear from you!</a>
                                    </p>
                                </li>

                                <li>
                                    <p>Stay tuned for lots more exiting updates as we empower society with the critical environmental tools to share data on pollution.</p>
                                </li>

                                <li>
                                    <p>P.S. Our <a href="https://gofundme.com/openlittermap" target="_blank">GoFundMe has reached 2%. We would really appreciate it if you could share it?</a></p>
                                </li>
