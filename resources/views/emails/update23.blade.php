                            <p style="font-family: sans-serif; font-size: 18px;">Hi everyone. Just a small update but a big one!</p>
                            <ul style="font-family: sans-serif; font-size: 19px;">
                                <li>
                                    <p>Did you notice our global map was really slow when you zoomed in and loaded too many points?</p>
                                    <p><strong>This has been fixed!</strong> We integrated webGL to change how the points are being rendered. Now even a mobile device can load huge amounts of point data.</p>
                                    <a href="https://openlittermap.com/global" target="_blank">See for yourself by zooming right in on the Global Map</a>
                                    <p>What does litter look like in your community? We need your help to find out!</p>
                                </li>

                                <li>
                                    <p>OpenLitterMap is growing! We are now getting about 500 images uploaded per day. On Good Friday, over 30 people signed up and created an account to share data and in March nearly 300 people signed up!</p>
                                </li>

                                <li>
                                    <p>This also means the costs are rising. We would really appreciate it if you could help our <a href="https://gofundme.com/openlittermap" target="_blank">GoFundMe</a>?</p>
                                </li>

                                <li>
                                    <p>We also need more volunteers that can help us add boxes to images to train the OpenLitterAi. All you need is some time and a keyboard!</p>
                                </li>

                                <li>
                                    <p>Our Slack channel has never been more active! If you are ready to be a part of the OpenLitterMap movement, <a href="https://join.slack.com/t/openlittermap/shared_invite/zt-fdctasud-mu~OBQKReRdC9Ai9KgGROw">Click here to join our chat and be a part of our open-source community</a></p>
                                    <p>We especially want to hear from new users. What was your first experience like? How could we do better? What would you like to change or see improved?</p>
                                </li>

                                <li>
                                    <p>We are currently in touch with 2 Universities who want to help with OLM research. This is so new and unexplored that currently, not even 1 university campus in the world has had their litter mapped. Who will be the first...?</p>
                                </li>

                                <li>
                                    <p>Several bug fixes to the iOS app made recently and more on the way.</p>
                                </li>

                                <li>
                                    <p>Seaspiracy was also released on Netflix. You should check it out, but citizen science is so new and unexplored it doesn't even mention it yet even though it does a good job at highlighting some of our environmental problems.</p>
                                </li>
                            </ul>
