                        <td>
                            <p style="font-family: sans-serif; font-size: 18px;">There are plenty of updates to share since our last email on the 4th of August 2021.</p>

                            <p style="font-family: sans-serif; font-size: 18px;">You can find most of them on our <a href="https://medium.com/@weeklyolm">weekly blog</a> but we will share the best bits here.</p>
                            <ul style="font-family: sans-serif; font-size: 19px;">
                                <li>
                                    <h2>Global leaderboard for all users. </h2>

                                    <p>
                                        Yes, finally! We now have a global leaderboard of all 6,100+ and growing members. Check it out and find your position at <a href="https://openlittermap.com/leaderboard" target="_blank">openlittermap.com/leaderboard</a>

                                        <br>

                                        <p>By default, new users are anonymous, so if you haven't already done so- visit settings and check your privacy options to see if your name or username are set to appear on the leaderboards.

                                        <br>

                                    <p>In the web-app settings, there are more options. You can choose to show a country flag and you can now include links to your social media accounts to promote them.</p>
                                </li>

                                <li>
                                    <h2>Promote links to your social media accounts on OpenLitterMap</h2>

                                    <p>The more data you upload, the more points you score, and the more visibility your accounts get.</p>

                                    <p>Social media accounts will now appear in every popup on the global map and for every user in the global leaderboard.</p>

                                    <a href="https://twitter.com/OpenLitterMap/status/1519764272699609094" target="_blank">See this twitter post for examples</a>
                                </li>

                                <li>
                                    <h2>Weekly community calls</h2>

                                    <p>Every Thursday, 6pm GMT, we host a weekly live zoom call where we code live, launch updates, and test new features with the community while planning for the future and having fun.</p>

                                    <p>Want to help shape the future direction of OpenLitterMap? <a href="https://openlittermap.com/community">Join us</a></p>
                                </li>

                                <li>
                                    <h2>Huge mobile app improvements.</h2>

                                    <p>It took a long time but the app is finally starting to work incredibly well!</p>

                                    <p>The photo gallery is now just totally smooth.</p>

                                    <p>You can now create a team and join a team on the app. You can see the leaderboard for all teams listed so far.</p>

                                    <p>There are a lot more new features and improvements left to add in, but the most recent update (v3.2.3) is really stable.</p>

                                    <p>Now that the app has reached stability, we will begin to work on new gamification features soon!</p>

                                    <a href="https://medium.com/@weeklyolm/weeklyolm-15-7th-april-2022-3f6d1953792c">Read more about recent app improvements here.</a>
                                </li>

                                <li>
                                    <h2>Huge global map improvements</h2>

                                    <p>Seriously, just look at how all of this global data just loads <b>instantly.</b></p>

                                    <a href="https://openlittermap.com/global" target="_blank">Open the Global Map</a>

                                    <p>All of the clusters are re-created every night at midnight.</p>

                                    <p>You can also change the data by year, or all time.</p>

                                    <p>Keep the global map open to see live notifications. Click a photo uploaded notification to zoom to that location and open the data!</p>

                                    <p>It's now possible to filter the global map point data by user, minDate and maxDate in the URL.</p>

                                    <p>Clusters of just 1 photos now appear on the global map. You can see how far North and South the openlittermap community has reached- incredible!</p>

                                    <p>Also added new options to sort locations <a href="https://twitter.com/OpenLitterMap/status/1522662606749806594">click here to open tweet</a></p>
                                </li>

                                <li>
                                    <h2>Growing global community</h2>

                                    <p>It took a long time, but openlittermap is after getting really stable lately, and now we are attracting a much more engaged community.</p>

                                    <p>Every day, new people are joining our <a href="https://join.slack.com/t/openlittermap/shared_invite/zt-fdctasud-mu~OBQKReRdC9Ai9KgGROw" target="_blank">Slack channel</a> and weekly community zoom calls.</p>

                                    <p>All data is going up. We are approaching 500,000 tags and recently passed 250,000 images.</p>

                                    <p>I have given dozens of presentations across universities + institutions, met with people from UNEP and even gave a presentation to my local Cork City Council</p>
                                </li>

                                <li>
                                    <h2>Sent a 12 page report about my work to various Irish government oireachtas committees</h2>

                                    <p>You can read the report <a href="https://docs.google.com/document/d/1icUoEqbhG8fbee0fpCrE8WA2cGB8TZkN39rPUd0I3j8" target="_blank">here</a></p>

                                    <p>Worryingly, both the committees on Education, Research, Science, and Innovation and "climate action and environment" had nothing to say. The committee on EU affairs did not even reply about using citizen science as a means of building Peace Through Science in zones of conflict (like Northern Ireland).</p>

                                    <p>In the last 15 years of this research there has been a total of €0 in funding to support the development of OpenLitterMap in Ireland.</p>
                                </li>

                                <li>
                                    <h2>Expect more updates soon!</h2>

                                    <p>OpenLitterMap is staring to work well and its growing, but there is a lot more that needs to be done.</p>

                                    <p>We hope you will join us by mapping and sharing data on litter and plastic pollution in your community</p>
                                </li>
                            </ul>
